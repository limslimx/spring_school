package poly.dto;

public class ScheduleDTO {

	private String collect_time;
	private String schedule_seq;
	private String day;
	private String time;
	private String team_home;
	private String team_away;
	private String score_home;
	private String score_away;
	
	
	public String getCollect_time() {
		return collect_time;
	}
	public void setCollect_time(String collect_time) {
		this.collect_time = collect_time;
	}
	public String getSchedule_seq() {
		return schedule_seq;
	}
	public void setSchedule_seq(String schedule_seq) {
		this.schedule_seq = schedule_seq;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTeam_home() {
		return team_home;
	}
	public void setTeam_home(String team_home) {
		this.team_home = team_home;
	}
	public String getTeam_away() {
		return team_away;
	}
	public void setTeam_away(String team_away) {
		this.team_away = team_away;
	}
	public String getScore_home() {
		return score_home;
	}
	public void setScore_home(String score_home) {
		this.score_home = score_home;
	}
	public String getScore_away() {
		return score_away;
	}
	public void setScore_away(String score_away) {
		this.score_away = score_away;
	}

	
	

}